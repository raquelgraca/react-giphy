import React from "react";
import ButtonFilter from "./button_filter";
import DropdownCard from "./dropdown_card";


class ButtonWithDd extends React.Component {
    constructor(props) {
        super(props);
        
        this.state = {
            open: false,
        };
    }
    
    toggleOpen = () => {
        this.setState({
        open: !this.state.open
        });
    }

    render() {
        return (
        <div>
          <ButtonFilter toggleOpen={this.toggleOpen} />
          <DropdownCard open={this.state.open}/>
        </div>
      );
    }
}

export default ButtonWithDd;
